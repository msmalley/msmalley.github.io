var flip_vertical = function(array)
{
    var new_array = JSON.parse(JSON.stringify(array));
    var param = new_array.pop();
    new_array.reverse();
    new_array.push(param);
    return new_array;
}

var flip_horizontal = function(array, needs_popping = true)
{
    var param = false;
    if(needs_popping)
    {
        param = array.pop();
    }
    var new_array = [];
    for(a = 0; a < array.length; a++)
    {
        var str = "";
        for(var i = array[a].length - 1; i >= 0; i--) 
        {
            var c = array[a][i];
            if(c == '!') str += '/';
            else if(c == '/') str+= '!';
            else if(c == '(') str+= ')';
            else if(c == ')') str+= '(';
            else if(c == '{') str+= '}';
            else if(c == '}') str+= '{';
            else str+= c;
        }
        new_array.push(str);
    }
    if(param)
    {
        new_array.push(param);
    }
    return new_array;
}

